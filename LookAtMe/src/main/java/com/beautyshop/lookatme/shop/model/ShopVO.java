package com.beautyshop.lookatme.shop.model;

public class ShopVO {
	private int i_shop;
	private int i_user;
	private String shop;
	private String phone;
	private String old_addr;
	private String new_addr;
	private int zip_code;
	private String cd;
	
	public int getI_shop() {
		return i_shop;
	}
	public void setI_shop(int i_shop) {
		this.i_shop = i_shop;
	}
	public int getI_user() {
		return i_user;
	}
	public void setI_user(int i_user) {
		this.i_user = i_user;
	}
	public String getShop() {
		return shop;
	}
	public void setShop(String shop) {
		this.shop = shop;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getOld_addr() {
		return old_addr;
	}
	public void setOld_addr(String old_addr) {
		this.old_addr = old_addr;
	}
	public String getNew_addr() {
		return new_addr;
	}
	public void setNew_addr(String new_addr) {
		this.new_addr = new_addr;
	}
	public int getZip_code() {
		return zip_code;
	}
	public void setZip_code(int zip_code) {
		this.zip_code = zip_code;
	}
	public String getCd() {
		return cd;
	}
	public void setCd(String cd) {
		this.cd = cd;
	}
	
		
	
}
