package com.beautyshop.lookatme.shop.model;

public class ShopPARAM extends ShopVO {
	private int i_pic;
	private int i_comment;
	private String proc_type;
	private String addr;
	private String searchTxt;
	
	
	public int getI_pic() {
		return i_pic;
	}
	public void setI_pic(int i_pic) {
		this.i_pic = i_pic;
	}
	public int getI_comment() {
		return i_comment;
	}
	public void setI_comment(int i_comment) {
		this.i_comment = i_comment;
	}
	public String getProc_type() {
		return proc_type;
	}
	public void setProc_type(String proc_type) {
		this.proc_type = proc_type;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getSearchTxt() {
		return searchTxt;
	}
	public void setSearchTxt(String searchTxt) {
		this.searchTxt = searchTxt;
	}
	
	
	
}
