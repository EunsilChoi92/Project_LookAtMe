package com.beautyshop.lookatme.comment.model;

public class CommentPARAM extends CommentVO {
	private int orderBy;
	private int startComment;
	private int commentPage;

	public int getOrderBy() {
		return orderBy;
	}

	public void setOrderBy(int orderBy) {
		this.orderBy = orderBy;
	}

	public int getStartComment() {
		return startComment;
	}

	public void setStartComment(int startComment) {
		this.startComment = startComment;
	}

	public int getCommentPage() {
		return commentPage;
	}

	public void setCommentPage(int commentPage) {
		this.commentPage = commentPage;
	}

	
	
	
	
}
